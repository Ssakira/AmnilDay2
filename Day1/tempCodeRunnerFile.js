//Logical AND
let a = 5;
let b = 10;
console.log(a < b && a == 5);
console.log(a > b && a == 5);
console.log(a > b && a == 10);
console.log("*************");
//Logical OR
let a = 5;
let b = 10;
console.log(a < b || a == 5);
console.log(a > b || a == 5);
console.log(a > b || a == 10);
console.log("*************");