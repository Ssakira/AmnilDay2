//Logical AND
let c = 5;
let d = 10;
console.log(c < d && c == 5);
console.log(c > d && c == 5);
console.log(c > d && c == 10);
console.log("*************");
//Logical OR
let e = 5;
let f = 10;
console.log(e < f || e == 5);
console.log(e > f || e == 5);
console.log(e > f || e == 10);
console.log("*************");

//Logical NOT
let a = 5;
let b = 5;
let h = "2";
console.log(!(a == b));
console.log(!(a === h));
