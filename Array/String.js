//charAt()
let str = "Sakira";
console.log(str.charAt(1));

//concat()
let str1 = "Hello";
let str2 = " World";
console.log(str1.concat(str2));

//replace()
console.log(str.replace("Sakira", "Javascript"));

//split()
let sentence = "Sakira plays Basketball";
console.log(sentence.split(" "));

//substr(start,length)
let name = "Sakira Maharjan";
console.log(name.substr(0, 6));

//subString(start,end)
let name1 = "Luffy Luf";
console.log(name1.substring(0, 6));

//trim()
let space = " There is a space in this line ";
console.log(space.trim());

//search()
console.log(space.search("space"));

